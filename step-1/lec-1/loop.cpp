#include<iostream>
using namespace std;

int main(){
    for (int i = 0; i < 5; i++)
    {
        cout<<"For : "<<i<<'\n';
    }

    int j = 0;
    while (j<=10)
    {
        cout<<"While : "<<j<<'\n';
        j++;
    }
    int k = 0;
    do
    {
        cout<<"Do While : "<<k<<'\n';
        k++;
    } while (k<=15);
    
    
    
}